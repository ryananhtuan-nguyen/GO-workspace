package main

import (
	"fmt"
	"log"
	"net/http"
	"time"
)

const delay = 10 * time.Millisecond

func printLetterByLetter(line string) {
	for _, char := range line {
		fmt.Print(string(char))
		time.Sleep(delay)
	}
	fmt.Println()
}

